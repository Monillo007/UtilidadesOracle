    private void btExaminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExaminarActionPerformed
        JFileChooser carpeta = new JFileChooser();
        carpeta.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        File rutaDir = null;
        if(!txDestino.getText().equals("")){
            rutaDir = new File(txDestino.getText().replace("\\", "\\\\"));
        }else if(new File("C:\\ARCHIVOS DE LUIS\\Archivos y documentos de LUIS\\NetBeans Projects\\").exists()){
            rutaDir = new File("C:\\ARCHIVOS DE LUIS\\Archivos y documentos de LUIS\\NetBeans Projects\\");
        }
        carpeta.setCurrentDirectory(rutaDir);
        int opcion = carpeta.showOpenDialog(this);
        if(opcion == JFileChooser.APPROVE_OPTION){
            txDestino.setText(carpeta.getSelectedFile().getAbsolutePath());
            String ruta = txDestino.getText().replace("\\", "\\\\");
            VariablesGlobales.setRutaDestino(ruta);
        }
    }//GEN-LAST:event_btExaminarActionPerformed

    private void btCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCrearActionPerformed
        if(validaciones()){
            VariablesGlobales.setNombreArchivo(txNombreArchivo.getText());
            VariablesGlobales.setImportString(txImport.getText());
            VariablesGlobales.setNombreFormulario(txNombreForm.getText());
            VariablesGlobales.setTituloTabla(txTituloTabla.getText());
            VariablesGlobales.setPaqClases(txPaqueteClase.getText());
            VariablesGlobales.setEsquema(txEsquema.getText());
            Procedimientos proc = new Procedimientos(VariablesGlobales.getCamposOcultos());
            proc.setProc("jsp");
            Thread hilo = new Thread(proc);
            hilo.start();
        }
}//GEN-LAST:event_btCrearActionPerformed

