    public boolean validaciones(){
       boolean valido = true;
       if(txDestino.getText().equals("")){
            JOptionPane.showMessageDialog(this, "No has elegido la carpeta destino.");
            btExaminar.doClick();
            valido = false;
        }else if(txNombreArchivo.getText().equals("")){
            JOptionPane.showMessageDialog(this, "No has escrito el nombre del archivo.");
            txNombreArchivo.requestFocusInWindow();
            valido = false;
        }else if(txNombreForm.getText().equals("")){
            JOptionPane.showMessageDialog(this, "No has escrito el nombre del formulario.");
            txNombreForm.requestFocusInWindow();
            valido = false;
        }else if(txTituloTabla.getText().equals("")){
            JOptionPane.showMessageDialog(this, "No has escrito el título de la tabla.");
            txTituloTabla.requestFocusInWindow();
            valido = false;
        }
       return valido;
    }

